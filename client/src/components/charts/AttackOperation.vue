const scrapeInformation = (search_term) => {
    const newData = store.attackRecord;
    let final = [];
    let ish = [];

    Object.keys(newData).forEach((ele) => {
        ish.push(ele);
    })
    final.push(ish);
    let interrupted_record = Object.fromEntries(Object.entries(newData[search_term]).filter(([key, value]) => value !== 'None'));
    Object.keys(newData).forEach((ele) => {
        let newtest = [];
        Object.keys(interrupted_record).forEach((key) => {
            newtest.push(newData[ele][key]);
        })
        final.push(newtest)
    })
    return final;
}

const addColors = (data) => {
    let copy = data;
    copy[0].push("color");
    const nameIndex = copy[0].indexOf("name");
    let colors = [];
    copy[nameIndex].forEach((name) => {
        if (name == "application")
        colors.push("green");
        else if (name == "network")
        colors.push("orange");
    })
    copy.push(colors)
    return copy;
}

const plotGraph = async () => {
    const attackNames = Array.from(new Set(Object.values(store.attackRecord.name)));
